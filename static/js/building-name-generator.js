generateNewBuildings = function () {
    output.html("");

    for (i = 0; i <= num; i++) {

        output.prepend("<p>"+buildingName()+"</p>");
    }
}

buildingName = function(){
    var final = "";
    var typeChance = Math.floor(Math.random() * 100);
    var randomPart1 = part1[Math.floor(Math.random() * part1.length)];
    var randomPart2 = part2[Math.floor(Math.random() * part2.length)];
    var randomRoadPrefix = roadPrefix[Math.floor(Math.random() * roadPrefix.length)];
    var randomRoadSuffix = roadSuffix[Math.floor(Math.random() * roadSuffix.length)];
    var randomBuildingType = buildingType[Math.floor(Math.random() * buildingType.length)];
    var randomName = names[Math.floor(Math.random() * names.length)];
    var randomDescription = description[Math.floor(Math.random() * description.length)];
    var randomNumber = Math.floor(Math.random() * 1000);

    if (typeChance <= 5){
        final = randomNumber + " " + randomPart1 + randomPart2 + " " + randomRoadSuffix;
    } else if(typeChance > 5 && typeChance <= 15) {
        final = randomNumber + " " + randomRoadPrefix + " " + randomPart1 + randomPart2;
    } else if(typeChance > 15 && typeChance <= 30) {
        final = randomName + " " + randomBuildingType;
    } else if(typeChance > 30 && typeChance <= 50) {
        final = randomDescription + " " + randomBuildingType;
    } else if(typeChance > 50 && typeChance <= 80) {
        final = companyPart1() + companyPart2() + " " + randomBuildingType;
    } else if(typeChance > 70 && typeChance <= 80) {
        final = randomNumber + " " + companyPart1() + companyPart2() + " " + randomRoadSuffix;
    } else if(typeChance > 80 && typeChance <= 95) {
        final = randomNumber + " " + randomRoadPrefix + " " + randomPart1 + randomPart2;
    } else {
        final = "The " + randomName;
    }

    return final;
}

