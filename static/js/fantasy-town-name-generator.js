newCityName = function(){
    var final = "";
    var typeChance = Math.floor(Math.random() * 100);
    var prefixChance = Math.floor(Math.random() * 100);


    if (typeChance < 45) {
        final = city1() + cityMiddle() + city2();
    } else {
        final = city1() + city2();
    }

    if(prefixChance < 20){
        final =  cityPrefix() + " " + final;
    } else if (typeChance >= 20 && typeChance < 35) {
        final = final + " Town";
    } else if (typeChance >= 35 && typeChance < 40) {
        final = final + "'s " + citySuffix();
    } else if (typeChance >= 40 && typeChance < 65) {
        final =  final + " " + citySuffix();
    }

    return final;
}

generateNewCityNames = function(){
    $(".city-name").each(function(){

        $(this).html("");

        $(this).prepend(newCityName());

    });
}

$(document).ready(function() {
    generateNewCityNames();
});