newCityName = function(){
    var final = "";
    var typeChance = Math.floor(Math.random() * 100);
    var prefixChance = Math.floor(Math.random() * 100);

    if(typeChance < 15){
        final =  planetName();
    } else if (typeChance >= 15 && typeChance < 45) {
        final = city1() + cityMiddle() + city2();
    } else {
        final = city1() + city2();
    }

    if(prefixChance < 20){
        final =  cityPrefix() + " " + final;
    } else if (typeChance >= 20 && typeChance < 40) {
        final = final + " City";
    } else if (typeChance >= 40 && typeChance < 65) {
        final =  final + " " + citySuffix();
    }

    return final;
}

