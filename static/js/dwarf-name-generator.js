generateNew = function(){
    output.html("");

    for (i = 0; i <= num; i++) {
        var prefixChance = Math.floor(Math.random() * 100);
        var suffixChance = Math.floor(Math.random() * 100);
        var firstTypeChance = Math.floor(Math.random() * 100);
        var lastTypeChance = Math.floor(Math.random() * 100);

        var first = "";
        var last = "";
        var final = "";

        //--first name
        if(firstTypeChance <= 40){
            //--using consonant
            var randomConsonant = consonants[Math.floor(Math.random() * consonants.length)];
            var randomFirstAfterConsonant = firstAfterConsonant[Math.floor(Math.random() * firstAfterConsonant.length)];

            first = randomConsonant + randomFirstAfterConsonant;
        } else if (firstTypeChance > 40 && firstTypeChance <= 55) {
            //--2 part name
            var randomFirstPart1 = firstPart1[Math.floor(Math.random() * firstPart1.length)];
            var randomFirstPart2 = firstPart2[Math.floor(Math.random() * firstPart2.length)];

            first = randomFirstPart1 + randomFirstPart2;
        } else if (firstTypeChance > 55 && firstTypeChance <=  70) {
            //--2 names
            var randomFirst = firstNames[Math.floor(Math.random() * firstNames.length)];

            first = randomFirst;

            randomFirst = firstNames[Math.floor(Math.random() * firstNames.length)];

            first = first + " " + randomFirst;
        } else {
            //--choose from name list
            var randomFirst = firstNames[Math.floor(Math.random() * firstNames.length)];
            first = randomFirst;
        }

        //--last name
        if(lastTypeChance <= 40){
            var randomConsonant = consonants[Math.floor(Math.random() * consonants.length)];
            var randomLastAfterConsonant = lastAfterConsonant[Math.floor(Math.random() * lastAfterConsonant.length)];

            last = randomConsonant + randomLastAfterConsonant;

        } else if(lastTypeChance > 40 && lastTypeChance <= 80){
            var randomLastPart1 = lastPart1[Math.floor(Math.random() * lastPart1.length)];
            var randomLastPart2 = lastPart2[Math.floor(Math.random() * lastPart2.length)];

            last = randomLastPart1 + randomLastPart2;

        } else {
            var randomLast = lastNames[Math.floor(Math.random() * lastNames.length)];
            last = randomLast;
        }

        final =  first + " " + last;

        //--does it have a prefix?
        if(prefixChance < 10){
            var randomPrefix = prefix[Math.floor(Math.random() * prefix.length)];
            final =  randomPrefix + " " + final;
        }

        //--does it have a suffix?
        if(suffixChance < 20){
            var randomSuffix = suffix[Math.floor(Math.random() * suffix.length)];
            final =  final + " " + randomSuffix;
        }

        output.prepend("<p>"+final+"</p>");
    }

}

generateNew();
